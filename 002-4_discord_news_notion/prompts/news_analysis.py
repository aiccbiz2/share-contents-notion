"""
AI 분석 + Notion 저장 프롬프트 (MCP 방식)
Claude CLI가 WebFetch로 URL을 직접 읽고 3관점 분석 후 Notion에 저장.
"""


def build_news_prompt(
    url: str,
    source: str,
    database_id: str,
    shared_by: str = "",
) -> str:
    return f"""아래 URL을 WebFetch로 읽고 분석하여 Notion에 저장하세요. 한국어로 작성합니다.

## URL
{url}

## Notion 데이터베이스 ID
{database_id}

## Notion 페이지 속성 (Properties)
- Name (Title): 기사/콘텐츠 제목 (한국어로 번역, 핵심 메시지가 드러나도록)
- URL (URL): {url}
- 언론사 (Select): {source}
- 저장일 (Date): 오늘 날짜
- 공유자 (Rich text): {shared_by or "Discord Bot"}
- Tag (Multi-select): 관련 태그 3~5개

## Notion 페이지 본문 (Blocks) — 아래 순서대로 정확히 작성

본문은 **[Version 1] 정리형**과 **[Version 2] 설명형** 두 버전을 순서대로 모두 작성한다.

### 1. 원문 링크
- bookmark 블록으로 원본 URL 삽입

### 2. Divider

### 3. 한 줄 요약 — Callout (💡, blue_background)
- 전체 내용을 한 문장으로 압축 (50자 이내)
- "누가 무엇을 했고, 왜 중요한지"가 드러나야 함

### 4. Divider

---

## [Version 1] 정리형 — Heading 1: "📋 정리형"

간결하고 빠르게 훑을 수 있는 축약 버전. 각 bullet은 1문장으로 핵심만.

### 5. 핵심 내용 — Heading 2: "핵심 내용"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드** — 1문장으로 핵심 팩트 요약 (수치·기업명·날짜 포함)

### 6. 시사점 — Heading 2: "시사점"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드** — 1문장으로 영향·변화 요약

### 7. 비판적 관점 — Heading 2: "비판적 관점"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드** — 1문장으로 반론·리스크 요약

### 8. Divider

---

## [Version 2] 설명형 — Heading 1: "📖 설명형"

맥락과 인과관계가 드러나는 서술형 분석. 기사 전문을 읽지 않아도 흐름과 의미를 이해할 수 있도록 충분히 설명.

### 9. 핵심 내용 — Heading 2: "핵심 내용"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드(2~4단어)** — 배경, 구체적 수치·날짜·기업명·인물·금액 등을 포함해 최소 2~3문장 서술형으로 설명
- "왜 발생했는지 → 무엇이 일어났는지 → 어떤 의미인지"가 드러나도록 작성
- 기사 속 구체적 숫자, 연도, 기업명, 정책명, 제품명 등을 반드시 포함

### 10. 시사점 — Heading 2: "시사점"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드** — "이 사건으로 인해 [누가] [어떤 영향]을 받으며, 그 결과 시장/산업/사회에 어떤 변화가 예상되는지"를 최소 2~3문장 서술형으로 설명
- 업계 트렌드, 경쟁 구도, 소비자·정책·투자 환경 변화 등 구조적 영향을 분석
- 가능하면 비교 사례나 과거 흐름과 연결

### 11. 비판적 관점 — Heading 2: "비판적 관점"
- 3~5개 bulleted_list_item
- 각 항목 형식: **굵은 키워드** — "~라고 하지만, 실제로는 ~" 또는 "~을 간과하고 있다. 왜냐하면 ~" 구조로 최소 2~3문장 서술형 논리적 반론 제시
- 반대 입장에서 설득력 있는 논리 전개 (시장 구조, 이해관계, 장기적 리스크, 과거 사례 등 근거 활용)

## 작성 원칙
- 모든 bullet은 **굵은 키워드** — 설명 형식 통일
- 정리형: 1문장 축약. 설명형: 최소 2~3문장 서술
- 기사 속 구체적 수치·날짜·기업명 등 팩트 반드시 포함
- 사실(기사 내용)과 해석(시사점·비판)을 구분
- 본문 접근 제한 시 가능한 정보로 최선을 다해 분석

## 완료 후
생성된 Notion 페이지 URL을 마지막 줄에 출력하세요.
형식: NOTION_URL: https://www.notion.so/...
"""
